            <h1>The Story Behind DEOVAL: From Humble Beginnings to Thriving Success</h1>
            <p>DEOVAL’s journey began with two hard working employees of a printing 
                press—De Ocampo, an operator, and Valeros, a designer. After facing 
                challenges in an unhealthy work environment, they decided it was time 
                for a change. Together, they resolved to start their own business. 
                With Valeros providing the capital and De Ocampo bringing his operational 
                expertise, DEOVAL was born—a name inspired by their shared surnames. <br><br>

                In 2015, they took their first step into entrepreneurship. But starting a 
                business wasn’t easy. With no established market or customer base, their only 
                tool was social media to promote their services. Despite the odds, their 
                determination paid off when a customer from Samar traveled to personally 
                confirm the legitimacy of their shop after seeing their online posts. <br><br>
            
                Fast forward to today, DEOVAL is approaching its 10th anniversary, a milestone 
                that reflects their dedication and hard work. Their secret? Never giving up, 
                staying committed to their goals, and keeping faith in God. DEOVAL’s inspiring 
                journey reminds us that with perseverance, trust, and a little faith, success 
                is always within reach.</p>
